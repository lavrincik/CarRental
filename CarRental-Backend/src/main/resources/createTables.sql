CREATE TABLE "CARS" (
  "ID"  INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
  "CARBRAND"  VARCHAR(70) NOT NULL,
  "DESCRIPTION" VARCHAR(300) NOT NULL,
  "DAILYPRICE"  DECIMAL(16,0) NOT NULL
);

CREATE TABLE "CUSTOMERS" (
  "ID"  INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
  "FULLNAME"  VARCHAR(100) NOT NULL,
  "ADDRESS" VARCHAR(150) NOT NULL,
  "PHONENUMBER" VARCHAR(20) NOT NULL
);

CREATE TABLE "LEASES" (
  "ID"  INT PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
  "LEASEDCARID" INT REFERENCES cars (id) ON DELETE CASCADE NOT NULL,
  "CUSTOMERID"  INT REFERENCES customers (id) ON DELETE CASCADE NOT NULL,
  "STARTDATE" DATE NOT NULL,
  "ENDDATE" DATE NOT NULL,
  "PRICE" DECIMAL(16,0) NOT NULL
);
